package projet;

public class Sqrt extends OperationUnaire  {
	public Sqrt(double num) {
		super(num);
	}
	
	
	public double Racine() {
		return Math.sqrt(num);
	}
	
	public double Calculer() {
		return Racine();
	}
	
	public double Cosinus() {
		return Cosinus();
	}
	
	public double Sinus() {
		return Sinus();
	}
	
	public double Logarithme() {
		return Logarithme();
	}
	
	public double Expo() {
		return Expo();
	}
	
	

}
