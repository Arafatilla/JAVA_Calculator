package projet;

public  class Multiplication extends OperationBinaire  {
	
	public Multiplication (double num1, double num2) {
		super (num1 , num2);
	}
	
	public double Mul() {
		return (num1*num2);
	}
	public double Calculer() {
		return Mul();
	}
	
	public double Add() {
		
		return Add();
	}
	
	public double Sous() {
		
		return Sous();
	}
	
	public double Div() {
		
		return Div();
	}

}
