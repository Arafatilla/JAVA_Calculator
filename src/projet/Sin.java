package projet;

public class Sin extends OperationUnaire  {
	public Sin (double num ) {
		super (num);
	}
	
	public double Sinus() {
		return Math.sin(num);
	}
	
	public double Calculer() {
		return Sinus();	
	}
	
	public double Cosinus() {
		return Cosinus();
	}
	
	public double Logarithme() {
		return Logarithme();
	}
	
	public double Expo() {
		return Expo();
	}
	
	public double Racine() {
		return Racine();
	}

}
