package projet;

public class Exp extends OperationUnaire {
	public Exp(double num) {
		super(num);
	}
	
	public double Expo() {
		return Math.exp(num);
	}
	
	
	public double Calculer() {
		return Expo();
	}
	
	public double Cosinus() {
		return Cosinus();
	}
	
	public double Sinus() {
		return Sinus();
	}
	
	public double Logarithme() {
		return Logarithme();
	}
	
	public double Racine() {
		return Racine();
	}
	

}
