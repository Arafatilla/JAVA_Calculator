package projet;

public   class Soustraction extends OperationBinaire  {
	
	public Soustraction (double num1, double num2) {
		super(num1, num2);
	}
	public double Sous() {
		return (num1 - num2);
	}
	public double Calculer() {
		return Sous();
	}
	
	public double Add() {
		
		return Add();
	}
	
	public double Mul() {
		
		return Mul();
	}
	
	public double Div() {
		
		return Div();
	}
	
	
	

}
