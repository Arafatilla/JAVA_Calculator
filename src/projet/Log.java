package projet;

public class Log extends OperationUnaire   {
	public Log(double num) {
		super(num);
	}
	
	public double Logarithme() {
		return Math.log(num);
	}
	
	public double Calculer() {
		return Logarithme();
	}
	
	public double Cosinus() {
		return Cosinus();
	}
	
	public double Sinus() {
		return Sinus();
	}
	
	public double Expo() {
		return Expo();
	}
	
	public double Racine() {
		return Racine();
	}
	

}
