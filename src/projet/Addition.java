package projet;

public class Addition extends OperationBinaire {
	public Addition(double num1, double num2) {
		super(num1,num2);
	}
	
	public double Add() {
		return(num1 + num2);
	}
	public double Calculer() {
		return Add();
	}
	
	public double Sous() {
		
		return Sous();
	}
	
	public double Mul() {
		
		return Mul();
	}
	
	public double Div() {
		
		return Div();
	}

}
